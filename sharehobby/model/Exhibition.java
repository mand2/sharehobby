package sharehobby.model;

public class Exhibition extends ExhibitionPlace{
	
	private int he_num;
	private String he_name;
	private String he_artist;
	private String he_cont;
	private String he_theme;
	
	
	public void setHe_num(int he_num) {
		this.he_num = he_num;
	}
	public void setHe_name(String he_name) {
		this.he_name = he_name;
	}
	public void setHe_artist(String he_artist) {
		this.he_artist = he_artist;
	}
	public void setHe_cont(String he_cont) {
		this.he_cont = he_cont;
	}
	public void setHe_theme(String he_theme) {
		this.he_theme = he_theme;
	}
	public int getHe_num() {
		return he_num;
	}	
	public String getHe_name() {
		return he_name;
	}
	public String getHe_artist() {
		return he_artist;
	}
	public String getHe_cont() {
		return he_cont;
	}
	public String getHe_theme() {
		return he_theme;
	}
	
	
}
